public class DataTypes {
    public static void main(String[] args) {

        //NÚmEROS ENTEROS
        //4 bytes
        int n = 1234567890;

        // para asignar los long se le agg al número una L al final
        //8 bytes
        long nL = 1234567890123456789L;

        //NÚMEROS DECIMALES
        //4 bytes
        double nD = 123.456;
        //Para asignar float se agg una F al final del número
        //8 bytes
        float nF = 123.45678945678F;



    }
}
